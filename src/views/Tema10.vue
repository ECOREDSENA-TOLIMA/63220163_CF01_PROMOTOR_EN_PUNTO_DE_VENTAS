d y competencia comunicativa. 

            p.text-small(numero="f" titulo="Demostración") Es el medio para mostrar o demostrar cómo actúa un producto.  La demostración hace pruebas o exhibe hechos para confirmar la acción del producto.  Para realizarla se requiere que el lugar donde se hace cuente con los elementos complementarios adecuados y en buen funcionamiento. Los trajes o vestidos que usa el promotor forman parte importe del ambiente de la demostración. Al igual que en las degustaciones, se hace necesario el manejo de equipos o accesorios a utilizar, lo que requiere de un entrenamiento previo con toda la información relativa a la demostración. <br><br> Cuando el promotor haga la demostración debe hacer énfasis en las características del producto, dando campo a las preguntas, a la solicitud de opiniones y a la oportunidad de realizar personalmente la operación por parte del interesado.  

