            p.text-small(numero="a" titulo="Precios de promoción") Ahorran dinero al cliente mediante la aplicación de un descuento sobre el precio regular del producto, o incluirse en el producto algún tipo de descuento. Generalmente se usa esta técnica para animar al consumidor a ensayar un nuevo producto, inducir a nuevos usuarios o persuadir a los clientes existentes de seguir comprando, pero aumentando sus compras. Esta técnica funciona bien cuando en el árbol de decisiones del consumidor prima el precio y hay poca lealtad a la marca. Los precios de promoción pueden incluir descuentos, reembolsos y cupones.

