            p.text-small(numero="d" titulo="Muestras para el consumidor") Se trata de llevar el producto a las manos del consumidor en forma gratuita en cantidades pequeñas. Esta técnica se usa especialmente cuando el producto es nuevo, o la marca no es muy conocida. Es una técnica costosa que debe manejarse cuidadosamente, en ocasiones se acompaña mediante encuestas de satisfacción para medir la percepción o aceptación del producto. Para este caso, el promotor de ventas debe prepararse de acuerdo al propósito diseñado en la campaña, identificando el cliente objetivo, y asegurando que la muestra esté en manos de clientes potenciales.  

